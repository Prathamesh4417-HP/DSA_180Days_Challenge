#include<bits/stdc++.h>
using namespace std;

int main(){

    int row = 5;
    int col = 5;

    for(int i=1;i<=row;i++)
    {
        for(int j=col;j>=1;j--)
        {
            cout<<j<<" ";
        }
        cout<<endl;
    }
}

// Output

// 5 4 3 2 1 
// 5 4 3 2 1 
// 5 4 3 2 1 
// 5 4 3 2 1 
// 5 4 3 2 1 